package exemploswhile;

public class Estudando {

	public static void main(String[] args) {
		
		int nota = 5;
		int aprovacao = 9;
		
		
		// O while valida a condição para depois executar o codigo dentro das chaves
		while(nota < aprovacao) {
			
			System.out.println("reprovado nota "+ nota);
			nota++;
			
		}
		
		    
		System.out.println("Aprovado");
		

	}

}
